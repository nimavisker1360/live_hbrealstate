export function normalizeWhatsAppNumber(value?: string | null) {
  const digits = (value || HB_LEAD_WHATSAPP).replace(/\D/g, "");

  if (digits.startsWith("00")) {
    return digits.slice(2);
  }

  if (digits.length === 10 && digits.startsWith("5")) {
    return `90${digits}`;
  }

  if (digits.length === 11 && digits.startsWith("0")) {
    return `90${digits.slice(1)}`;
  }

  return digits;
}

export function buildWhatsAppUrl({
  text,
  whatsapp,
}: {
  text: string;
  whatsapp?: string | null;
}) {
  const number = normalizeWhatsAppNumber(whatsapp);

  return `https://wa.me/${number}?text=${encodeURIComponent(text)}`;
}

export function getConsultantByAgent(agent?: {
  id?: string | null;
  name?: string | null;
}) {
  return (
    HB_CONSULTANTS.find((consultant) => consultant.id === agent?.id) ??
    HB_CONSULTANTS.find(
      (consultant) =>
        consultant.name.toLowerCase() === agent?.name?.trim().toLowerCase(),
    ) ??
    null
  );
}
